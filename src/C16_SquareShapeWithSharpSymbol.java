public class C16_SquareShapeWithSharpSymbol {
    public static void main(String[] args) {
/*

Create a figure just shown as below by using only one # in print

Aşağıdaki şekilde çıktı veren programı print içerisinde
sadece bir tane # kullanarak yazıdırınız

            #####
            #####
            #####
            #####
            #####
 */
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.print("#");
            }
            System.out.println();
        }
    }
}
